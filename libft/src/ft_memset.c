#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	int		i;
	char	*temp;

	i = 0;
	temp = (char *)s;
	while (n > 0)
	{
		temp[i] = c;
		i++;
		n--;
	}
	return (s);
}
