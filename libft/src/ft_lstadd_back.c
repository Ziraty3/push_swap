#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new_lst)
{
	t_list	*aux;

	if (!*lst)
	{
		*lst = new_lst;
		return ;
	}
	aux = ft_lstlast(*lst);
	aux->next = new_lst;
}
